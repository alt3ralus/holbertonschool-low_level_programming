#include "prototypes.h"
#include <stdio.h>
/**
 * add - sum
 * @a: int
 * @b: int.
 * mod - modul op.
 * Return: int.
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sum
 * @a: int
 * @b: int.
 * mod - modul op.
 * Return: int.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - sum
 * @a: int
 * @b: int.
 * mod - modul op.
 * Return: int.
 */

int mul(int a, int b)
{
	return (a *b);
}
/**
 * div - sum
 * @a: int
 * @b: int.
 * mod - modul op.
 * Return: int.
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - sum
 * @a: int
 * @b: int.
 * mod - modul op.
 * Return: int.
 */

int mod(int a, int b)
{
	return (a % b);
}
